  @override
  Widget build(BuildContext context) {
    final favoriteProvider = Provider.of<FavoriteProvider>(context);
    final favoriteHymns =
        favoriteProvider.getAllFavoriteHymns().entries.toList().reversed.toList( );

    return Scaffold(
      backgroundColor:
          Provider.of<ThemeProvider>(context).themeData.colorScheme.secondary,
      appBar: AppBar(
        title: Text(
          'ကြိုက်နှစ်သက်မှုများ',
          style: TextStyle(fontSize: 16),
        ),
        centerTitle: true,
      ),
      body: favoriteHymns.isEmpty
          ? Center(
              child: Text(
                'ကြိုက်နှစ်သက်မှုမရှိသေးပါ!',
                style: TextStyle(
                    color: Provider.of<ThemeProvider>(context)
                        .themeData
                        .colorScheme
                        .inversePrimary),
              ),
            )
          : Padding(
            padding: const EdgeInsets.all(8.0),
            child: ListView.builder(
                itemCount: favoriteHymns.length,
                itemBuilder: (context, index) {
                  final hymnEntry = favoriteHymns[index];
                  return _buildPDFViewer(hymnEntry.key, hymnEntry.value,
                      favoriteProvider, index + 1);
                },
              ),
          ),
    );
  }

  Widget _buildPDFViewer(
      String key, String hymn, FavoriteProvider favoriteProvider, int index) {
    return InkWell(
      onTap: () async {
        int StartIndex = key.indexOf('(') + 1;
        int endIndex = key.indexOf(')');
        String File_index = key.substring(StartIndex, endIndex);
        print(key.contains('ဓမ္မသီချင်း'));
        print(
            'asset/images/MH${(indexing.indexOf(File_index) + 1).toString().padLeft(3, '0')}-1.jpg');
        Navigator.push(
          context,
          MaterialPageRoute(
              builder: (context) => key.contains('ဓမ္မသီချင်း')
                  ? MyFavoritePdfViewer(
                      pdfPath:
                          'asset/images/MH${(indexing.indexOf(File_index) + 1).toString().padLeft(3, '0')}-1.jpg',
                      number: key,
                      hymn: hymn,
                      type: "Favorite",
                      index: index,
                    )
                  : MyFavoritePdfViewer(
                      pdfPath:
                          'asset/images/RP${(indexing.indexOf(File_index) + 1).toString().padLeft(2, '0')}-1.jpg',
                      number: key,
                      hymn: hymn,
                      type: "Favorite",
                      index: index,
                    )),
        );
      },
      child: Card(
        child: ListTile(
          leading: Text(key),
          title: Text(hymn),
          trailing: IconButton(
              onPressed: () => favoriteProvider.removeHymn(key),
              icon: Icon(Icons.delete)),
        ),
      ),
    );
  }
